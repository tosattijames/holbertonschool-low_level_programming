/**
 * _isdigit - checks for a digit (0 through 9
 *
 *@c: character input
 *
 * Return: 1 if true, 0 false
 */
int _isdigit(int c)
{
if
((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
{
return (0);
}
else if (c >= '0' && c <= '9')
{
return (1);
}
else
{
return (0);
}
return (0);
}
